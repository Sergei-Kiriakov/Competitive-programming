a, b, x = map(int, input().split())
if x % 2 == 1:
    s = "0" * (a - (x + 1) // 2) + "01" * ((x + 1) // 2) + "1" * (b - (x + 1) // 2)
else:
    if a > b:
        s = "0" * (a - (x + 1) // 2 - 1) + "01" * ((x + 1) // 2) + "1" * (b - (x + 1) // 2) + "0"
    else:
        s = "1" + "0" * (a - (x + 1) // 2) + "01" * ((x + 1) // 2) + "1" * (b - (x + 1) // 2 - 1)
print(s)